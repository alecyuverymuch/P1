
public class Sym {
	
	// String to contain the Sym type
	private String type;
	
	/**
	 * Constructor, creates a Sym with the given type
	 * @param type
	 */
	public Sym(String type) {
		this.type = type;
	}
	
	/**
	 * returns the Sym's type
	 * @return type
	 */
	public String getType() {
		return type;
	}
	
	/**
	 * returns the object as a string
	 * @return type
	 */
	public String toString() {
		return type;
	}
}
